public class Employee {
    private String name;
    private double salary;
    private int year;
    private int month;
    private int day;


    public Employee(String name, double salary, int year, int month, int day){
        this.name = name;
        this.salary = salary;
        this.year = year;
        this.month = month;
        this.day = day;
    }
}
